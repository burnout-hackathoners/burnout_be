  def generate_uniq_random_ids(count)
    unique_random_ids = []
    while unique_random_ids.length < count
      random_id = rand(1..1000)
      unique_random_ids.push(random_id) if unique_random_ids.exclude?(random_id)
    end
    unique_random_ids
  end
